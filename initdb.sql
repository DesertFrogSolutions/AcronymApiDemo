CREATE TABLE IF NOT EXISTS acronym (
  acronym_id serial NOT NULL PRIMARY KEY,
  name text NOT NULL,
  description text NOT NULL,
  created_at timestampz NOT NULL DEFAULT NOW(),
  updated_at timestampz NOT NULL DEFAULT NOW()
